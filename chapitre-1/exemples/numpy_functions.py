import numpy as np


# zeros
numpy_array = np.zeros(3)
print("zeros:\n", numpy_array)

# ones
numpy_array = np.ones((3, 4))
print("ones:\n", numpy_array)

# full
numpy_array = np.full((3, 4), 2)
print("full:\n", numpy_array)

# eye
numpy_array = np.eye(3)
print("eye:\n", numpy_array)

# arange
numpy_array = np.arange(3)
print("arange:\n", numpy_array)

# linspace
numpy_array = np.linspace(0, 1, 3)
print("linspace:\n", numpy_array)
